const pi = 3.14159265;

function add(n1, n2) {
    return (n1 + n2);
}

function multiply(n1, n2) {
    return (n1 * n2);
}

function subtract(n1, n2) {
    return (n1 - n2);
}

function divide(n1, n2) {
    return (n1 / n2);
}

function triplePi() {
    return (3 * pi);
}

export default pi;
export {add, multiply, subtract, divide, triplePi};